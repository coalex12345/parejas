package estructurascontrol;

public class Ej10elbueno {

	public static void main(String[] args) {
		int suma=0;
		int producto=1;
		for(int i=1; i<=5; i++) {
			suma=suma+i;
			producto=producto*i;}
		System.out.print("la suma es "+suma+" ");
		System.out.println("\n");
		System.out.println("el producto es "+producto+" ");
		
	}
			
		
}


